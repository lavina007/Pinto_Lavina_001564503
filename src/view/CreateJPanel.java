    private void btnPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPhotoActionPerformed
        // TODO add your handling code here:
        JFileChooser imgFile = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());        
        // do not allow users to select any file type 
        imgFile.setAcceptAllFileFilterUsed(false); 
        // set a title for the dialog 
        imgFile.setDialogTitle("Select an image for profile (extensions allowed: PNJ, JPG, JPEG)"); 
        // restricting extensions for user selection
        FileNameExtensionFilter restrict = new FileNameExtensionFilter("Only .png or .jpg or .jpeg files", "png", "jpg","jpeg"); 
        imgFile.addChoosableFileFilter(restrict); 

 

        // invoke the showsOpenDialog function to show the save dialog 
        int r = imgFile.showOpenDialog(null); 
        
        // if the user selects a file 
        if (r == JFileChooser.APPROVE_OPTION) { 
            // set the label to the path of the selected file 
            btnPhoto.setText(imgFile.getSelectedFile().getAbsolutePath()); 
            person.setPhoto(btnPhoto.getText());
    } 
    }//GEN-LAST:event_btnPhotoActionPerformed


