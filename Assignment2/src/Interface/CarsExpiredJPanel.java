     public void populateTable() {
         DefaultTableModel dtm = (DefaultTableModel) tblExpiredCars.getModel();
         dtm.setRowCount(0);
         int count = 0;
         Object row [] = new Object[6];
         
         for (UberCars uberCars: uberCarsDirectory.getUberCarsDirectory()){
             if(!uberCars.isValidility()){
             row[0] = uberCars;
             row[1] = uberCars.getModelNumber();
             row[2] = uberCars.getManufacturer();
             row[3] = uberCars.isAvailability();
             row[4] = uberCars.getSeats();
             row[5] = uberCars.getCity();
             dtm.addRow(row);
             count++;
             }
         }
         
         txtExpiredCars.setText(String.valueOf(count));
    }
     
