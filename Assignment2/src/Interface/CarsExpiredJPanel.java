    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int selectedRow = tblExpiredCars.getSelectedRow();

        if (selectedRow >= 0){
            UberCars uberCars = (UberCars) tblExpiredCars.getValueAt(selectedRow, 0);
            ViewCarsJPanel1 viewCarsJPanel = new ViewCarsJPanel1(userProcessContainer, uberCars, uberCarsDirectory);
            userProcessContainer.add ("ViewCarsJPanel", viewCarsJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else{
            JOptionPane.showMessageDialog(null,"Please selecte a row from the table", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewActionPerformed

