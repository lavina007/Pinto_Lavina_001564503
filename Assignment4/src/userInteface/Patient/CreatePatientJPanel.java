    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        // TODO add your handling code here:
        if (checkBlankInput())
        {
            Patient patient = new Patient();
            patient.setPatientID(txtPatientId.getText());
            patient.setPrimaryDoctorName(txtDoctorName.getText());
            patient.setPrefferedPharmacy(txtMedicalName.getText());
            //Adding Patient to Person
            person.setPatient(patient);
            JOptionPane.showMessageDialog(this, "Patient added!!", "Update", 
                    JOptionPane.INFORMATION_MESSAGE);
            txtPatientId.setText("");
            txtDoctorName.setText("");
            txtMedicalName.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please enter all values",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addJButtonActionPerformed
    
    private Boolean checkBlankInput()
    {
        if(txtPatientId.getText().length()==0
                ||txtDoctorName.getText().length()==0
                ||txtMedicalName.getText().length()==0)
        {
            return false;
        }
        else{
            return true;
        }
    }
    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        // TODO add your handling code here:
        workAreaContainer.remove(this);
        CardLayout card = (CardLayout) workAreaContainer.getLayout();
        card.previous(workAreaContainer);
    }//GEN-LAST:event_backJButtonActionPerformed
    

